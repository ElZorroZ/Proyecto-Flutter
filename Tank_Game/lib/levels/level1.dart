import 'dart:async';
import 'package:flame/components.dart';
import 'package:flame/game.dart';
import 'package:flame_tiled/flame_tiled.dart';

class Level extends World {
  late TiledComponent level;

  @override
  Future<void> onLoad() async {
    try {
      level = await TiledComponent.load(
        "assets/tiles/nivel1.tmx",
        Vector2.all(50), // Asegúrate de que el tamaño del tile es correcto :v
      );

      add(level);

      await super.onLoad(); // Asegúrate de llamar a super.onLoad() después de agregar componentes uwuw
    } catch (e) {
      print("Error loading level: $e");
    }
  }
}

